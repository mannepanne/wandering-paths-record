// ABOUT: One-off export script — dumps all Supabase data to JSON backup and
// ABOUT: D1-compatible SQL INSERT statements. Used for the one-time Supabase
// ABOUT: → D1 migration in early 2026. HISTORICAL — not part of the current
// ABOUT: app (no Supabase connection). Kept as a record of the migration tool.
//
// Usage:
//   SUPABASE_SERVICE_ROLE_KEY=<key> node scripts/export-supabase-to-d1.js
//
// Outputs to db_backup/ (gitignored):
//   - raw-restaurants.json         — exact Supabase data
//   - raw-restaurant-addresses.json
//   - raw-restaurant-visits.json
//   - d1-seed.sql                  — D1-compatible INSERT statements

import { createClient } from '@supabase/supabase-js'
import { writeFileSync, mkdirSync } from 'fs'
import { join, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = join(__dirname, '..')
const OUT_DIR = join(ROOT, 'db_backup')

// --- Config ---

const SUPABASE_URL = 'https://drtjfbvudzacixvqkzav.supabase.co'
const SERVICE_ROLE_KEY = process.env.SUPABASE_SERVICE_ROLE_KEY

if (!SERVICE_ROLE_KEY) {
  console.error('ERROR: SUPABASE_SERVICE_ROLE_KEY env var is required.')
  console.error('Find it in Supabase → Settings → API → service_role key.')
  console.error('Run as: SUPABASE_SERVICE_ROLE_KEY=<key> node scripts/export-supabase-to-d1.js')
  process.exit(1)
}

const supabase = createClient(SUPABASE_URL, SERVICE_ROLE_KEY, {
  auth: { persistSession: false }
})

// --- Helpers ---

function toIso(value) {
  if (!value) return null
  return value  // Supabase already returns ISO-8601 strings
}

function toJsonArray(value) {
  // PostgreSQL arrays come from Supabase as JS arrays — serialise for SQLite TEXT column
  if (!value) return null
  if (Array.isArray(value)) return JSON.stringify(value)
  return value  // already a string somehow, leave it
}

function toBoolInt(value) {
  if (value === null || value === undefined) return 0
  return value ? 1 : 0
}

function sqlStr(value) {
  if (value === null || value === undefined) return 'NULL'
  return `'${String(value).replace(/'/g, "''")}'`
}

function sqlNum(value) {
  if (value === null || value === undefined) return 'NULL'
  return String(value)
}

// --- Fetch all data ---

async function fetchAll(table) {
  const { data, error } = await supabase.from(table).select('*')
  if (error) throw new Error(`Failed to fetch ${table}: ${error.message}`)
  console.log(`  ${table}: ${data.length} rows`)
  return data
}

// --- Generate D1 INSERT SQL ---

function restaurantToSql(r) {
  return `INSERT INTO restaurants VALUES (
  ${sqlStr(r.id)}, ${sqlStr(r.name)}, ${sqlStr(r.address)}, ${sqlStr(r.website)},
  ${sqlStr(r.status)}, ${sqlStr(r.personal_appreciation)}, ${sqlStr(r.description)},
  ${sqlNum(r.visit_count ?? 0)}, ${sqlStr(r.cuisine)}, ${sqlStr(r.cuisine_primary)},
  ${sqlStr(r.cuisine_secondary)}, ${sqlStr(r.style)}, ${sqlStr(r.venue)},
  ${sqlStr(toJsonArray(r.specialty))}, ${sqlStr(toJsonArray(r.must_try_dishes))},
  ${sqlStr(r.price_range)}, ${sqlStr(r.atmosphere)}, ${sqlStr(r.dietary_options)},
  ${sqlStr(r.source)}, ${sqlStr(r.source_url)},
  ${sqlNum(r.latitude)}, ${sqlNum(r.longitude)},
  ${sqlNum(r.public_rating)}, ${sqlNum(r.public_rating_count)},
  ${sqlStr(r.public_review_summary)},
  ${sqlStr(toIso(r.public_review_summary_updated_at))},
  ${sqlStr(toIso(r.public_review_latest_created_at))},
  ${sqlStr(toIso(r.created_at))}, ${sqlStr(toIso(r.updated_at))}
);`
}

function addressToSql(a) {
  return `INSERT INTO restaurant_addresses VALUES (
  ${sqlStr(a.id)}, ${sqlStr(a.restaurant_id)}, ${sqlStr(a.location_name)},
  ${sqlStr(a.full_address)}, ${sqlStr(a.city)}, ${sqlStr(a.country)},
  ${sqlNum(a.latitude)}, ${sqlNum(a.longitude)}, ${sqlStr(a.phone)},
  ${sqlStr(toIso(a.created_at))}, ${sqlStr(toIso(a.updated_at))}
);`
}

function visitToSql(v) {
  return `INSERT INTO restaurant_visits VALUES (
  ${sqlStr(v.id)}, ${sqlStr(v.restaurant_id)},
  ${sqlStr(v.visit_date)}, ${sqlStr(v.rating)},
  ${sqlStr(v.experience_notes)}, ${sqlStr(v.company_notes)},
  ${toBoolInt(v.is_migrated_placeholder)},
  ${sqlStr(toIso(v.created_at))}, ${sqlStr(toIso(v.updated_at))}
);`
}

// --- Main ---

async function main() {
  console.log('Fetching data from Supabase...')
  const restaurants = await fetchAll('restaurants')
  const addresses = await fetchAll('restaurant_addresses')
  const visits = await fetchAll('restaurant_visits')

  mkdirSync(OUT_DIR, { recursive: true })

  // Raw JSON backups
  writeFileSync(join(OUT_DIR, 'raw-restaurants.json'), JSON.stringify(restaurants, null, 2))
  writeFileSync(join(OUT_DIR, 'raw-restaurant-addresses.json'), JSON.stringify(addresses, null, 2))
  writeFileSync(join(OUT_DIR, 'raw-restaurant-visits.json'), JSON.stringify(visits, null, 2))
  console.log('\nJSON backups written to db_backup/')

  // D1-compatible SQL
  const lines = [
    '-- D1 seed data — generated by export-supabase-to-d1.js',
    `-- Exported: ${new Date().toISOString()}`,
    `-- Restaurants: ${restaurants.length}, Addresses: ${addresses.length}, Visits: ${visits.length}`,
    '',
    'PRAGMA foreign_keys = OFF;',
    '',
    '-- Restaurants',
    ...restaurants.map(restaurantToSql),
    '',
    '-- Addresses',
    ...addresses.map(addressToSql),
    '',
    '-- Visits',
    ...visits.map(visitToSql),
    '',
    'PRAGMA foreign_keys = ON;',
  ]

  writeFileSync(join(OUT_DIR, 'd1-seed.sql'), lines.join('\n'))
  console.log('D1 SQL seed written to db_backup/d1-seed.sql')
  console.log('\nDone. Check db_backup/ — these files are gitignored.')
}

main().catch(err => {
  console.error('Export failed:', err.message)
  process.exit(1)
})
